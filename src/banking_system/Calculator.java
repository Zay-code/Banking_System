    private void btnDivideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDivideActionPerformed
        num = Float.parseFloat(edtDisplay.getText());
        switch (operation){
            case "n":
                result = num;
                break;
            
            case "/":
                result = result / num;
                break;
            case "*":
                result = result * num;
                break;
            case "-":
                result = result - num;
                break;
            case "+":
                result = result + num;
                break;
        }
        operation = "/"; 
        edtDisplay.setText("");
    }//GEN-LAST:event_btnDivideActionPerformed

